# ===== Narrative analysis generator =====
def generate_analysis_text(name, sectors, raw, cd):
    top_sector = max(sectors.items(), key=lambda x: x[1]['score'])
    bot_sector = min(sectors.items(), key=lambda x: x[1]['gap'])
    weaknesses = [s for s, d in sectors.items() if d['status'] == 'متأخر']
    strengths = [s for s, d in sectors.items() if d['status'] == 'متقدم']

    lines = [f'<div style="font-size:12px;color:#374151;line-height:1.9">']

    # Opening by rank context
    comp = raw.get('composite_index', 0)
    lines.append(f'<p style="margin:0 0 8px 0"><strong>الملخص التنموي:</strong> محافظة {name} ')

    # Population context
    pop = raw.get('population', 0)
    pop_note = f'يبلغ عدد سكانها {pop:,} نسمة'
    area = raw.get('area', 0)
    if area:
        pop_note += f' على مساحة {area:,.0f} كم²'
    lines.append(f'({pop_note}). ')

    # Top strength
    ts = top_sector[1]
    lines.append(f'أفضل القطاعات أداءً هو <strong>{top_sector[0]}</strong> بنقاط {ts["score"]:.1f}')

    # Specific data for strength sectors
    if top_sector[0] == 'التعليم':
        lines.append(f'حيث يبلغ عدد المدارس {raw.get("total_schools",0)} والتحاق ثانوي {raw.get("enrollment_secondary",0):.1f}%')
    elif top_sector[0] == 'الصحة':
        beds = raw.get('beds_per_10k', 0)
        docs = raw.get('specialist_doctors', 0)
        lines.append(f'بـ {beds:.1f} سرير/10k و{docs:,} طبيب اختصاص')
    elif top_sector[0] == 'السياحة':
        hotels = raw.get('hotels', 0)
        tourism_emp = raw.get('tourism_employment', 0)
        lines.append(f'بـ {hotels} فندق و{tourism_emp:,} عامل في القطاع السياحي')
    elif top_sector[0] == 'المؤشرات الاقتصادية':
        inv = raw.get('total_investment_m', 0)
        jobs = raw.get('investment_jobs', 0)
        income = cd.get('avg_individual_income', 0)
        lines.append(f'(استثمار {inv:.1f} مليون د.أ، {jobs:,} وظيفة متوقعة، متوسط دخل {income:.0f} د.أ)')
    elif top_sector[0] == 'المياه والصرف الصحي':
        w = raw.get('water_per_capita_lpd', 0)
        s = raw.get('sanitation_network_pct', 0)
        lines.append(f'(حصة الفرد {w:.1f} لتر/يوم، نسبة صرف صحي {s:.1f}%)')
    elif top_sector[0] == 'البنية التحتية':
        roads = raw.get('total_road_km', 0)
        lines.append(f'(شبكة طرق {roads:,.0f} كم)')
    elif top_sector[0] == 'التنمية الاجتماعية':
        aid = cd.get('aid_recipient_pct', 0)
        lines.append(f'(نسبة مستفيدي NAF {aid:.1f}%)')

    # Gap analysis - bottom sector
    bs = bot_sector[1]
    lines.append(f'، بينما القطاع الأكثر تأخراً هو <strong>{bot_sector[0]}</strong> بفجوة {bs["gap"]:.1f} نقطة عن المتوسط الوطني')

    if bot_sector[0] == 'التعليم':
        r = raw.get('schools_gov_rented', 0)
        d = raw.get('schools_gov_double_shift', 0)
        tot = raw.get('total_schools', 1)
        lines.append(f'({r} مدرسة مستأجرة، {d} مدرسة فترتين من أصل {tot})')
    elif bot_sector[0] == 'الصحة':
        beds = raw.get('beds_per_10k', 0)
        docs = raw.get('specialist_doctors', 0)
        lines.append(f'(فقط {beds:.1f} سرير/10k و{docs} طبيب اختصاص)')
    elif bot_sector[0] == 'المؤشرات الاقتصادية':
        unemp = cd.get('unemployment_rate', 0) * 100
        income = cd.get('avg_individual_income', 0)
        lines.append(f'(بطالة {unemp:.1f}%، متوسط دخل {income:.0f} د.أ/سنة)')
    elif bot_sector[0] == 'الزراعة':
        agri = raw.get('agri_workers', 0)
        lines.append(f'({agri:,} عامل زراعي)')
    elif bot_sector[0] == 'السياحة':
        hotels = raw.get('hotels', 0)
        emp = raw.get('tourism_employment', 0)
        lines.append(f'(نشاط سياحي محدود: {hotels} فندق، {emp} عامل)')
    elif bot_sector[0] == 'المياه والصرف الصحي':
        w = raw.get('water_per_capita_lpd', 0)
        s = raw.get('sanitation_network_pct', 0)
        lines.append(f'(حصة الفرد {w:.1f} لتر/يوم، صرف صحي {s:.1f}%)')
    elif bot_sector[0] == 'التنمية الاجتماعية':
        aid = cd.get('aid_recipient_pct', 0)
        lines.append(f'(نسبة مستفيدي المعونة {aid:.1f}%)')

    lines.append('.')

    # Contextual risk factors
    ctx_parts = []
    unemp = cd.get('unemployment_rate')
    if unemp is not None:
        ctx_parts.append(f'البطالة {unemp*100:.1f}%')
    ref_pct = cd.get('refugee_pct')
    if ref_pct is not None and ref_pct > 1:
        ctx_parts.append(f'نسبة لاجئين {ref_pct:.1f}%')
    income = cd.get('avg_individual_income')
    if income is not None:
        ctx_parts.append(f'متوسط الدخل {income:.0f} د.أ')

    if ctx_parts:
        lines.append(f'<br><br><strong>محاور سياقية:</strong> {" | ".join(ctx_parts)}.')
        if weaknesses:
            lines.append(f' القطاعات الأكثر احتياجاً للتدخل: <strong>{", ".join(weaknesses[:3])}</strong>')

    lines.append('</p></div>')
    return ''.join(lines)


